f_number = int(input("Enter first number: "))
s_number = int(input("Enter second number: "))
operation = input("Enter a sign: ")
if operation == "+":
    print(f_number + s_number)
elif operation == "-":
    print(f_number - s_number)
elif operation == "*":
    print(f_number * s_number)
elif operation == "/":
    print(f_number / s_number)
